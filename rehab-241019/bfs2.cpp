int dx[] = {-1, 0, 1, 0};
int dy[] = {0, -1, 0, 1};
const int maxn = 200;
#define mp(i, j) input_map_nonconst[i - 1][j - 1]
#define pii pair<int, int>
int Solution(const vector<vector<int>> &input_map, int input_day) {
    (void)input_map;
    (void)input_day;
    // write here
    vector<vector<int>> &input_map_nonconst = const_cast<vector<vector<int>>&>(input_map);
    int n = (int)input_map.size();

    vector<vector<vector<ll>>> ans(n + 1, vector<vector<ll>>(n + 1, vector<ll>(maxn + 1, 0)));
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (mp(i, j) == 2) {
                queue<pii> q;
                q.push(pii(i, j));
                vector<vector<ll>> dis(n + 1, vector<ll>(n + 1, 0));
                vector<vector<ll>> vis(n + 1, vector<ll>(n + 1, 0));
                vis[i][j] = 1;
                while (!q.empty()) {
                    int ux = q.front().first, uy = q.front().second;
                    q.pop();
                    if (dis[ux][uy] + 1 > input_day)
                        break;
                    for (int di = 0; di < 4; di++) {
                        int nx = ux + dx[di], ny = uy + dy[di];
                        if (nx < 1 || nx > n || ny < 1 || ny > n || mp(nx, ny) == 1 || vis[nx][ny])
                            continue;
                        vis[nx][ny] = 1;
                        dis[nx][ny] = dis[ux][uy] + 1;
                        q.push(pii(nx, ny));
                        if (mp(nx, ny) >= 3) {
                            ans[i][j][0] += mp(nx, ny);
                            mp(nx, ny) = 0;
                        }
                    }
                }
            }
        }
    }
    ll res = 0;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (mp(i, j) == 2)
                res = max(res, ans[i][j][0]);
        }
    }
    return (int)res;
}

class CRC32 {
  private:
    static vector<uint32_t> table;

    static void InitializeTable() {
        const uint32_t polynomial = 0xEDB88320;
        table.resize(256);

        for (uint32_t i = 0; i < 256; i++) {
            uint32_t crc = i;
            for (uint32_t j = 8; j > 0; j--) {
                if (crc & 1)
                    crc = (crc >> 1) ^ polynomial;
                else
                    crc >>= 1;
            }
            table[i] = crc;
        }
    }

  public:
    static uint32_t Compute(const string &text) {
        if (table.empty())
            InitializeTable();

        uint32_t crc = 0xFFFFFFFF;

        for (unsigned char c : text) {
            uint8_t index = static_cast<uint8_t>((crc & 0xFF) ^ c);
            crc = (crc >> 8) ^ table[index];
        }

        return ~crc;
    }
};

