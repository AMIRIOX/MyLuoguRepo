constexpr int dx[] = {-1, 0, 1, 0};
constexpr int dy[] = {0, -1, 0, 1};
const int maxn = 200;
#define mp(i, j) input_map[i - 1][j - 1]
#define pii pair<int, int>

int Solution(const vector<vector<int>> &input_map, int input_day) {
    (void)input_map;
    (void)input_day;
    // write here
    int n = (int)input_map.size();

    vector<vector<ll>> ans(n + 10, vector<ll>(n + 10, 0));
    vector<vector<ll>> _min(n + 10, vector<ll>(n + 10, 0x3f3f3f3f));
    vector<vector<pii>> _argmin(n + 10, vector<pii>(n + 10, pii(0, 0)));
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (mp(i, j) == 2) {
                deque<pii> q;
                q.push_back(pii(i, j));
                vector<vector<ll>> dis(n + 1, vector<ll>(n + 1, 0));
                vector<vector<ll>> vis(n + 1, vector<ll>(n + 1, 0));
                vis[i][j] = 1;
                while (!q.empty()) {
                    int ux = q.front().first, uy = q.front().second;
                    q.pop_front();
                    if (dis[ux][uy] + 1 > input_day)
                        continue;
                    for (int di = 0; di < 4; di++) {
                        int nx = ux + dx[di], ny = uy + dy[di];
                        if (nx < 1 || nx > n || ny < 1 || ny > n || mp(nx, ny) == 1 || vis[nx][ny])
                            continue;
                        int val = mp(nx, ny);
                        vis[nx][ny] = 1;
                        dis[nx][ny] = dis[ux][uy] + 1;
                        q.push_back(pii(nx, ny));
                        if(val >= 3 && dis[nx][ny] < _min[nx][ny]) {
                            int oi = _argmin[nx][ny].first, oj = _argmin[nx][ny].second;
                            ans[oi][oj] -= val;
                            ans[i][j] += val;
                            _argmin[nx][ny] = pii(i, j);
                            _min[nx][ny] = dis[nx][ny];
                        }
                    }
                }
            }
        }
    }
    ll res = 0;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (mp(i, j) == 2)
                res = max(res, ans[i][j]);
        }
    }
    return (int)res;
}

class CRC32 {
  private:
    static vector<uint32_t> table;

    static void InitializeTable() {
        const uint32_t polynomial = 0xEDB88320;
        table.resize(256);

        for (uint32_t i = 0; i < 256; i++) {
            uint32_t crc = i;
            for (uint32_t j = 8; j > 0; j--) {
                if (crc & 1)
                    crc = (crc >> 1) ^ polynomial;
                else
                    crc >>= 1;
            }
            table[i] = crc;
        }
    }

  public:
    static uint32_t Compute(const string &text) {
        if (table.empty())
            InitializeTable();

        uint32_t crc = 0xFFFFFFFF;

        for (unsigned char c : text) {
            uint8_t index = static_cast<uint8_t>((crc & 0xFF) ^ c);
            crc = (crc >> 8) ^ table[index];
        }

        return ~crc;
    }
};

