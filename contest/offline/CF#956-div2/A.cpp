#include <bits/stdc++.h>
using namespace std;

void solve() {
    int k = 0; cin >> k;
    for(int i = 1; i <= k; i++) {
        cout << i << " ";
    }
    cout << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t = 1; cin >> t;
    while(t--) solve();
    return 0;
}
